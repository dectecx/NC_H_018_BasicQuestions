// 4.使用雙層for顯示下列圖案:
// *****
// ****
// ***
// **
// *
// ============================================================
Console.WriteLine("============================================================");
Console.WriteLine("Q:使用雙層for顯示下列圖案");
Console.WriteLine("A:");
// 一共5排(從0開始算起)
for (int i = 0; i < 5; i++)
{
    // 每排的星星數量 = 5 - 當前位於第幾排
    for (int j = 0; j < 5 - i; j++)
    {
        Console.Write("*");
    }
    // 換行
    Console.WriteLine();
}