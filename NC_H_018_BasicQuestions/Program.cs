// 1.平板電腦一台15000元，每星期儲存1250元。寫一程式使用do...while計算需要幾個星期才能儲存足夠的錢購買平板電腦。
// ============================================================
Console.WriteLine("============================================================");
Console.WriteLine("Q:平板電腦一台15000元，每星期儲存1250元。寫一程式使用do...while計算需要幾個星期才能儲存足夠的錢購買平板電腦。");
// 電腦價格
double price = 15000;
// 每星期存錢
double save = 1250;
// 需要週數
double weekCount = 0;
// 每過一週就+1，並把價格減去存錢金額，直到價格小於零為止
do
{
    weekCount = weekCount + 1;
    price = price - save;
} while (price > 0);
Console.WriteLine("A:共需要" + weekCount + "個星期");

